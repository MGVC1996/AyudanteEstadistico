    public void AgregarFiltro() {
        selecArchivo.setFileFilter(new FileNameExtensionFilter("Excel (*.xls)", "xls"));
        selecArchivo.setFileFilter(new FileNameExtensionFilter("Excel (*.xlsx)", "xlsx"));
    }
    private void Jlbl_btn_Seleccionar_ArchivoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_Jlbl_btn_Seleccionar_ArchivoMouseClicked

        contAccion++;
        if (contAccion == 1) {
            AgregarFiltro();
        }

        if (selecArchivo.showDialog(null, "Seleccionar archivo") == JFileChooser.APPROVE_OPTION) {
            archivo = selecArchivo.getSelectedFile();
            if (archivo.getName().endsWith("xls") || archivo.getName().endsWith("xlsx")) {
                JOptionPane.showMessageDialog(null,
                        Data.Importar(archivo, JtDatos) + "\n Formato ." + archivo.getName().substring(archivo.getName().lastIndexOf(".") + 1),
                        "IMPORTAR EXCEL", JOptionPane.INFORMATION_MESSAGE);
                Jlbl_btn_realizar_analisis.setVisible(true);
            } else {
                JOptionPane.showMessageDialog(null, "Elija un formato valido.");
            }
        }

        //LLENAR LA MATRIZ DE DATOS
        int NColumnas = JtDatos.getColumnCount();
        int NFilas = JtDatos.getRowCount();

        double Datos[][] = new double[NFilas][NColumnas];
        String TitulosdeDatos[] = new String[NColumnas];
        for (int j = 0; j < NColumnas; j++) {
            for (int i = 0; i < NFilas; i++) {
                Datos[i][j] = Double.parseDouble(JtDatos.getValueAt(i, j).toString());
            }
        }

        for (int j = 0; j < NColumnas; j++) {
            TitulosdeDatos[j] = JtDatos.getColumnName(j);
        }
        Data.set_Numero_de_Filas(NFilas);
        Data.setNumero_de_columnas(NColumnas);
        Data.set_Matriz_de_datos(Datos);
        Data.setVector_de_titulos(TitulosdeDatos);


    }//GEN-LAST:event_Jlbl_btn_Seleccionar_ArchivoMouseClicked

