    private void Jlbl_btn_mostrar_resultados_pantalla_analisisMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_Jlbl_btn_mostrar_resultados_pantalla_analisisMouseClicked
        int NColumnas = JtDatos.getColumnCount();
        int NFilas = JtDatos.getRowCount();
        double Datos[][] = new double[NFilas][NColumnas];
        String TitulosdeDatos[] = new String[NColumnas];
        for (int j = 0; j < NColumnas; j++) {
            for (int i = 0; i < NFilas; i++) {
                Datos[i][j] = Double.parseDouble(JtDatos.getValueAt(i, j).toString());
            }
        }

        for (int j = 0; j < NColumnas; j++) {
            TitulosdeDatos[j] = JtDatos.getColumnName(j);
        }
        OE.set_Numero_de_Filas(NFilas);
        OE.setNumero_de_columnas(NColumnas);
        OE.set_Matriz_de_datos(Datos);
        OE.setVector_de_titulos(TitulosdeDatos);
        OE.setColumna_seleccionada(cbx_Titulos.getSelectedIndex());
        OE.setOperacion_elegida(cbx_Operacion_seleccionada.getSelectedIndex());
        OE.setPoblacion_o_muestra(cbx_poblacion_o_muestra.getSelectedIndex());
        OE.setVector_operativo(cbx_Titulos.getSelectedIndex());
        //Agregar Data a la tabla resultado

        dtm = (DefaultTableModel) JtResultados.getModel();

        if (cbx_Operacion_seleccionada.getSelectedIndex() != 0) {
            dtm.addRow(OE.devolver_resultado());
        } else {

            for (int i = 1; i < 12; i++) {
                OE.setOperacion_elegida(i);
                dtm.addRow(OE.devolver_resultado());
            }
        }
        //Hacer el Grafico Circular
        DefaultPieDataset datos_a_graficar = new DefaultPieDataset();
        int[] vector_de_frecuencias = new int[OE.getVector_operativo().length];
        int contador = 0;
        for (int i = 0; i < OE.getVector_operativo().length; i++) {
            for (int j = 0; j < OE.getVector_operativo().length; j++) {
                if (OE.getVector_operativo()[i] == OE.getVector_operativo()[j]) {
                    contador++;
                }
            }
            vector_de_frecuencias[i] = contador;
            contador = 0;
        }
        int Variable_Otros = 0;
        for (int i = 0; i < OE.getVector_operativo().length; i++) {
            if (vector_de_frecuencias[i] == 1) {
                Variable_Otros++;
            } else {
                datos_a_graficar.setValue("[" + OE.getVector_operativo()[i] + "]", vector_de_frecuencias[i]);
            }
        }
        datos_a_graficar.setValue("Otros", Variable_Otros);

        JFreeChart grafico_circular = ChartFactory.createPieChart3D(
                OE.getVector_de_titulos()[cbx_Titulos.getSelectedIndex()],
                datos_a_graficar,
                true,
                true,
                false
        );
        ChartPanel panel = new ChartPanel(grafico_circular);
        panel.setMouseWheelEnabled(true);
        panel.setPreferredSize(new Dimension(400, 273));
        JPanel_Graficas.setLayout(new BorderLayout());
        JPanel_Graficas.add(panel, BorderLayout.NORTH);
        pack();
        repaint();
        JpPanelGlobal.setSelectedIndex(3);
    }//GEN-LAST:event_Jlbl_btn_mostrar_resultados_pantalla_analisisMouseClicked

